  /**
   * Get the query to be used to display the EC5 patient listing
   *
   * @return String
   */
  public static String getEc5CombinedQuery() {
    String query =
        "SELECT pe.person_id As patient_id, "
            + "pid.identifier AS NID, "
            + "concat(ifnull(pn.given_name,''),' ',ifnull(pn.middle_name,''),' ',ifnull(pn.family_name,'')) AS Name, "
            + "DATE_FORMAT(pe.birthdate, '%d-%m-%Y') AS birthdate, "
            + "IF(pe.birthdate_estimated = 1, 'Yes','No') AS Estimated_dob, "
            + "pe.gender AS Sex, "
            + "DATE_FORMAT(pe.date_created, '%d-%m-%Y %H:%i:%s') AS First_entry_date, "
            + "DATE_FORMAT(pe.date_changed, '%d-%m-%Y %H:%i:%s') AS Last_updated, "
            + "DATE_FORMAT(pg.date_enrolled, '%d-%m-%Y') AS date_enrolled, "
            + "case "
            + "when ps.state = 9 then 'DROPPED FROM TREATMENT' "
            + "when ps.state = 6 then 'ACTIVE ON PROGRAM' "
            + "when ps.state = 10 then 'PATIENT HAS DIED' "
            + "when ps.state = 8 then 'SUSPENDED TREATMENT' "
            + "when ps.state = 7 then 'TRANSFERED OUT TO ANOTHER FACILITY' "
            + "when ps.state = 29 then 'TRANSFERRED FROM OTHER FACILTY' "
            + "end AS state, "
            + "DATE_FORMAT(ps.start_date, '%d-%m-%Y') AS state_date, "
            + "DATE_FORMAT(pe.death_date,'%d-%m-%Y') As death_date_demographic, "
            + "DATE_FORMAT(deadFichaResumo.death_date,'%d-%m-%Y') As death_date_resumo, "
            + "DATE_FORMAT(deadFichaClinica.death_date,'%d-%m-%Y') As death_date_clinica, "
            + "MIN(DATE_FORMAT(requestLabDate.dataPedidoLab, '%d-%m-%Y')) AS dataPedidoLab, "
            + "MIN(DATE_FORMAT(colheitaLabDate.dataColheitaLab, '%d-%m-%Y')) AS dataColheitaLab, "
            + "DATE_FORMAT(requestLabDate.dataLabForm, '%d-%m-%Y %H:%i:%s') AS dataLabFormPedido, "
            + "DATE_FORMAT(requestLabDate.dataLabCreation, '%d-%m-%Y %H:%i:%s') AS  dataLabCreation, "
            + "MIN(DATE_FORMAT(colheitaFSRDate.dataLabFSR, '%d-%m-%Y')) AS dataColheitaFSR, "
            + "DATE_FORMAT(colheitaFSRDate.dataFSRCreation, '%d-%m-%Y %H:%i:%s') AS dataFSRCreation, "
            + "l.name location_name "
            + "FROM person pe "
            + "left join person peObito on pe.person_id=peObito.person_id and peObito.voided=0 and peObito.death_date IS NOT NULL and peObito.death_date<=:endDate "
            + "left join ( "
            + "SELECT pg.patient_id AS patient_id, ps.start_date As death_date FROM patient p "
            + "INNER JOIN patient_program pg ON p.patient_id=pg.patient_id "
            + "INNER JOIN patient_state ps ON pg.patient_program_id=ps.patient_program_id "
            + "WHERE p.voided =0 "
            + "AND pg.program_id = 2 "
            + "AND pg.voided=0 "
            + "AND ps.voided=0 "
            + "AND ps.state=10 AND "
            + "pg.location_id IN(:location) "
            + "AND ps.start_date IS NOT NULL "
            + "AND ps.end_date IS NULL "
            + "AND ps.start_date<=:endDate "
            + ") deadPrograma on pe.person_id=deadPrograma.patient_id "
            + "left join "
            + "( "
            + "SELECT p.patient_id AS patient_id, o.obs_datetime AS death_date FROM patient p "
            + "INNER JOIN encounter e ON p.patient_id = e.patient_id "
            + "INNER JOIN obs o ON e.encounter_id = o.encounter_id "
            + "WHERE p.voided = 0 AND e.voided = 0 AND o.voided = 0 AND o.concept_id=6272 "
            + "AND value_coded = 1366 "
            + "AND e.encounter_type=53 "
            + "AND e.location_id IN(:location) "
            + "AND o.obs_datetime<=:endDate "
            + ") deadFichaResumo on pe.person_id=deadFichaResumo.patient_id "
            + "left join ( "
            + "SELECT p.patient_id AS patient_id, o.obs_datetime AS death_date FROM patient p "
            + "INNER JOIN encounter e ON p.patient_id = e.patient_id "
            + "INNER JOIN obs o ON e.encounter_id = o.encounter_id "
            + "WHERE p.voided = 0 AND e.voided = 0 AND o.voided = 0 AND o.concept_id=6273 "
            + "AND value_coded = 1366 "
            + "AND e.encounter_type=6 "
            + "AND e.location_id IN(:location) "
            + "AND o.obs_datetime<=:endDate "
            + ") deadFichaClinica on pe.person_id=deadFichaClinica.patient_id "
            + "left join ( "
            + "select pn1.* from person_name pn1 "
            + "inner join ( "
            + "select person_id,min(person_name_id) id from person_name "
            + "where voided=0 "
            + "group by person_id "
            + ") pn2 "
            + "where pn1.person_id=pn2.person_id and pn1.person_name_id=pn2.id "
            + ") pn on pn.person_id=pe.person_id "
            + "left join ( "
            + "select pid1.* from patient_identifier pid1 "
            + "inner join ( "
            + "select patient_id,min(patient_identifier_id) id from patient_identifier "
            + "where voided=0 "
            + "group by patient_id "
            + ") pid2 "
            + "where pid1.patient_id=pid2.patient_id and pid1.patient_identifier_id=pid2.id "
            + ") pid on pid.patient_id=pe.person_id "
            + "LEFT JOIN ( "
            + "SELECT p.patient_id as patient_id, o.value_datetime as dataPedidoLab,e.encounter_datetime dataLabForm, e.date_created dataLabCreation,l.name locationPedidoLab FROM patient p "
            + "INNER JOIN encounter e on e.patient_id=p.patient_id "
            + "INNER JOIN obs o on o.encounter_id=e.encounter_id "
            + "INNER JOIN location l on l.location_id=e.location_id "
            + "WHERE e.encounter_type=13 AND o.concept_id=6246  AND o.voided=0 AND e.voided=0 and o.value_datetime between :startDate and :endDate "
            + ")requestLabDate  on requestLabDate.patient_id=pe.person_id "
            + "LEFT JOIN ( "
            + "SELECT p.patient_id as patient_id, o.value_datetime as dataColheitaLab,e.encounter_datetime dataLabForm,e.date_created dataLabCreation, l.name locationColheitaLab FROM patient p "
            + "INNER JOIN encounter e on e.patient_id=p.patient_id "
            + "INNER JOIN obs o on o.encounter_id=e.encounter_id "
            + "INNER JOIN location l on l.location_id=e.location_id "
            + "WHERE e.encounter_type=13 AND o.concept_id=23821  AND o.voided=0 AND e.voided=0 and o.value_datetime between :startDate and :endDate "
            + ")colheitaLabDate on colheitaLabDate.patient_id=pe.person_id "
            + "LEFT JOIN ( "
            + "SELECT p.patient_id as patient_id, o.value_datetime as dataColheitaFSR,e.encounter_datetime dataLabFSR,e.date_created dataFSRCreation, l.name locationColheitaFSR FROM patient p "
            + "INNER JOIN encounter e on e.patient_id=p.patient_id "
            + "INNER JOIN obs o on o.encounter_id=e.encounter_id "
            + "INNER JOIN location l on l.location_id=e.location_id "
            + "WHERE e.encounter_type=51 AND o.concept_id=23821  AND o.voided=0 AND e.voided=0 and o.value_datetime between :startDate and :endDate "
            + ")colheitaFSRDate on colheitaFSRDate.patient_id=pe.person_id	 "
            + "left join  patient_program pg ON pe.person_id=pg.patient_id and pg.program_id=2 and pg.location_id IN(:location) "
            + "left join  patient_state ps ON pg.patient_program_id=ps.patient_program_id and ps.start_date IS NOT NULL AND ps.end_date IS NULL "
            + " LEFT JOIN encounter e1 on e1.patient_id =pe.person_id "
            + " LEFT JOIN location l on l.location_id=e1.location_id "
            + "where pe.voided=0 "
            + "and ( peObito.person_id is not null or "
            + "deadPrograma.patient_id is not null or "
            + "deadFichaResumo.patient_id is not null or "
            + "deadFichaClinica.patient_id is not null "
            + ") and ( "
            + "( "
            + "requestLabDate.dataPedidoLab>peObito.death_date or "
            + "requestLabDate.dataPedidoLab>deadPrograma.death_date or "
            + "requestLabDate.dataPedidoLab>deadFichaResumo.death_date or "
            + "requestLabDate.dataPedidoLab>deadFichaClinica.death_date "
            + ") "
            + "OR "
            + "( "
            + "colheitaLabDate.dataColheitaLab>peObito.death_date or "
            + "colheitaLabDate.dataColheitaLab>deadPrograma.death_date or "
            + "colheitaLabDate.dataColheitaLab>deadFichaResumo.death_date or "
            + "colheitaLabDate.dataColheitaLab>deadFichaClinica.death_date "
            + ") "
            + "OR "
            + "( "
            + "colheitaFSRDate.dataColheitaFSR>peObito.death_date or "
            + "colheitaFSRDate.dataColheitaFSR>deadPrograma.death_date or "
            + "colheitaFSRDate.dataColheitaFSR>deadFichaResumo.death_date or "
            + "colheitaFSRDate.dataColheitaFSR>deadFichaClinica.death_date "
            + ") "
            + ") "
            + "GROUP BY pe.person_id ";
    return query;
  }

