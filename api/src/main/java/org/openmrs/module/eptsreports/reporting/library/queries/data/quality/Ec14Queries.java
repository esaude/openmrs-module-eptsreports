package org.openmrs.module.eptsreports.reporting.library.queries.data.quality;

public class Ec14Queries {

  /**
   * Get the query for EC14 patient listing
   *
   * @return String
   */
  public static String getEc14CombinedQuery() {
    String query =
        " SELECT "
            + " pe.person_id As patient_id, "
            + " pid.identifier AS NID, "
            + " concat(ifnull(pn.given_name,''),' ',ifnull(pn.middle_name,''),' ',ifnull(pn.family_name,'')) AS Name, "
            + " DATE_FORMAT(pe.birthdate, '%d-%m-%Y') AS birthdate, "
            + " IF(pe.birthdate_estimated = 1, 'Yes','No') AS Estimated_dob, "
            + " pe.gender AS Sex, "
            + " DATE_FORMAT(pe.date_created, '%d-%m-%Y %H:%i:%s') AS First_entry_date, "
            + " DATE_FORMAT(pe.date_changed, '%d-%m-%Y %H:%i:%s') AS Last_updated, "
            + " DATE_FORMAT(programState.date_enrolled, '%d-%m-%Y %H:%i:%s') AS date_enrolled, "
            + " case "
            + " when programState.state = 9 then 'DROPPED FROM TREATMENT' "
            + " when programState.state = 6 then 'ACTIVE ON PROGRAM' "
            + " when programState.state = 10 then 'PATIENT HAS DIED' "
            + " when programState.state = 8 then 'SUSPENDED TREATMENT' "
            + " when programState.state = 7 then 'TRANSFERED OUT TO ANOTHER FACILITY' "
            + " when programState.state = 29 then 'TRANSFERRED FROM OTHER FACILTY' "
            + "  end AS state, "
            + " DATE_FORMAT(programState.start_date, '%d-%m-%Y %H:%i:%s') AS state_date, "
            + " obsvaluedatetime.FormType, "
            + " DATE_FORMAT(obsvaluedatetime.encounter_datetime, '%d-%m-%Y %H:%i:%s') AS encounter_date, "
            + " obsvaluedatetime.name AS Concept_Name, "
            + " DATE_FORMAT(obsvaluedatetime.concept_value, '%d-%m-%Y %H:%i:%s') AS concept_value, obsvaluedatetime.location_name "
            + " FROM "
            + " person pe "
            + " inner join "
            + " (	select pn1.* "
            + " from person_name pn1 "
            + " inner join "
            + " ( "
            + " select person_id, min(person_name_id) id "
            + " from person_name "
            + " where voided = 0 "
            + " group by person_id "
            + " ) pn2 "
            + " where pn1.person_id = pn2.person_id and pn1.person_name_id = pn2.id "
            + " ) pn on pn.person_id = pe.person_id "
            + " inner join "
            + " (   select pid1.* "
            + " from patient_identifier pid1 "
            + " inner join "
            + " ( "
            + " select patient_id, min(patient_identifier_id) id "
            + " from patient_identifier "
            + " where voided = 0 "
            + " group by patient_id "
            + " ) pid2 "
            + " where pid1.patient_id = pid2.patient_id and pid1.patient_identifier_id = pid2.id "
            + " ) pid on pid.patient_id = pe.person_id "
            + " inner join "
            + " ( "
            + " SELECT p.patient_id AS patient_id, o.obs_datetime AS obs_datetime, e.encounter_datetime, l.name  location_name, "
            + " o.value_datetime as concept_value, cn.name, 'Ficha Resumo' AS FormType "
            + " FROM patient p "
            + " INNER JOIN encounter e ON p.patient_id = e.patient_id "
            + " INNER JOIN location l on l.location_id = e.location_id "
            + " INNER JOIN obs o ON e.encounter_id = o.encounter_id "
            + " INNER JOIN concept_name cn on cn.concept_id = o.concept_id AND cn.voided = 0 and cn.concept_name_type = 'FULLY_SPECIFIED' "
            + " WHERE p.voided = 0 AND e.voided = 0 AND o.voided = 0 "
            + " AND cn.locale = 'pt' "
            + " AND e.encounter_type = 53 "
            + " AND e.location_id IN (:location) "
            + " AND o.value_datetime is not null "
            + " AND o.value_datetime < '1985-01-01 00:00:00' "
            + " AND e.encounter_datetime between :startDate AND :endDate "
            + " UNION "
            + " SELECT p.patient_id AS patient_id, o.obs_datetime AS obs_datetime, e.encounter_datetime, l.name  location_name, "
            + " o.value_datetime as concept_value, cn.name, 'Ficha Clinica' AS FormType "
            + " FROM patient p "
            + " INNER JOIN encounter e ON p.patient_id = e.patient_id "
            + " INNER JOIN location l on l.location_id = e.location_id "
            + " INNER JOIN obs o ON e.encounter_id = o.encounter_id "
            + " INNER JOIN concept_name cn on cn.concept_id = o.concept_id AND cn.voided = 0 and cn.concept_name_type = 'FULLY_SPECIFIED' "
            + " WHERE p.voided = 0 AND e.voided = 0 AND o.voided = 0 "
            + " AND cn.locale = 'pt' "
            + " AND e.encounter_type = 6 "
            + " AND e.location_id IN (:location) "
            + " AND o.value_datetime is not null "
            + " AND o.value_datetime < '1985-01-01 00:00:00' "
            + " AND e.encounter_datetime between :startDate AND :endDate "
            + " UNION "
            + " Select p.patient_id AS patient_id, o.obs_datetime AS obs_datetime, e.encounter_datetime, l.name  location_name, "
            + " o.value_datetime as concept_value, cn.name, 'Fila' AS FormType "
            + " from patient p "
            + " INNER JOIN encounter e on p.patient_id = e.patient_id "
            + " INNER JOIN location l on l.location_id = e.location_id "
            + " INNER JOIN obs o ON e.encounter_id = o.encounter_id "
            + " INNER JOIN concept_name cn on cn.concept_id = o.concept_id AND cn.voided = 0 and cn.concept_name_type = 'FULLY_SPECIFIED' "
            + " where p.voided = 0 and e.voided = 0 and e.encounter_type = 18 and e.location_id IN (:location) "
            + " AND cn.locale = 'pt' "
            + " AND o.value_datetime is not null "
            + " AND o.value_datetime < '1985-01-01 00:00:00' "
            + " AND e.encounter_datetime between :startDate AND :endDate "
            + " UNION "
            + " SELECT p.patient_id AS patient_id, o.obs_datetime AS obs_datetime, e.encounter_datetime, l.name location_name, "
            + " o.value_datetime as concept_value, cn.name, 'RECEPCAO - LEVANTOU TARV' AS FormType "
            + " FROM patient p "
            + " INNER JOIN encounter e on p.patient_id = e.patient_id "
            + " INNER JOIN obs o on e.encounter_id = o.encounter_id "
            + " INNER JOIN concept_name cn on cn.concept_id = o.concept_id AND cn.voided = 0 and cn.concept_name_type = 'FULLY_SPECIFIED' "
            + " INNER JOIN location l on l.location_id = e.location_id "
            + " INNER JOIN  patient_program pg ON p.patient_id = pg.patient_id and pg.program_id = 2 and pg.location_id IN (:location) "
            + " INNER JOIN  patient_state ps ON pg.patient_program_id = ps.patient_program_id and ps.start_date IS NOT NULL AND ps.end_date IS NULL "
            + " where p.voided = 0 and e.voided = 0 and o.voided = 0 and e.encounter_type = 52 "
            + " AND e.location_id IN (:location) "
            + " AND cn.locale = 'pt' "
            + " AND o.value_datetime is not null "
            + " AND o.value_datetime < '1985-01-01 00:00:00' "
            + " AND e.encounter_datetime between :startDate AND :endDate "
            + " ) obsvaluedatetime on pe.person_id = obsvaluedatetime.patient_id "
            + " LEFT JOIN (SELECT pg.patient_id, pg.date_enrolled, ps.state, max(ps.start_date) AS start_date FROM patient_program pg "
            + " INNER JOIN patient_state ps ON pg.patient_program_id = ps.patient_program_id "
            + " AND ps.start_date IS NOT NULL "
            + " AND ps.end_date IS NULL "
            + " AND pg.program_id = 2 "
            + " AND pg.location_id IN (:location) "
            + " GROUP BY pg.patient_id "
            + " ) AS programState ON pe.person_id = programState.patient_id "
            + " where pe.voided = 0 and obsvaluedatetime.patient_id is not null ";
    return query;
  }
}
